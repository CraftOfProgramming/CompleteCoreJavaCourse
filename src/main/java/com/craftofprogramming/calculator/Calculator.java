package com.craftofprogramming.calculator;

public class Calculator {
    public long add(int a, int b) {
        return a + b;
    }

    public long subtract(int a, int b) {
        return a - b;
    }

    public long multiply(int a, int b) {
        return a * b;
    }

    public long divide(int a, int b) {
        return a / b;
    }
}
